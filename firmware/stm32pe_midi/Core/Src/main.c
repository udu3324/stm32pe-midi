uint16_t map_mT_to_u16(float val) {
	if (val < -50.0f)
		val = -50.0f;
	if (val > 50.0f)
		val = 50.0f;
	return (uint16_t) ((val + 50.0f) * 10.0f);  // Maps to 0â€“1000
}

